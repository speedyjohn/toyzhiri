package org.example.toy_zhiri.attribute.enums;

/**
 * Стратегия сравнения значения атрибута при фильтрации клиентом.
 * Определяет, как клиентский фильтр матчится с атрибутами варианта услуги.
 */
public enum MatchStrategy {

    /**
     * Точное равенство значений.
     * Пример: цвет авто = белый → матч, только если car_color = "WHITE".
     */
    SINGLE_EQ,

    /**
     * Значение варианта больше или равно клиентскому.
     * Пример: клиент хочет машину не старше 2020 → матч, если car_year >= 2020.
     */
    SINGLE_GTE,

    /**
     * Значение варианта меньше или равно клиентскому.
     * Пример: клиент ищет кейтеринг с минимальным заказом не более 50 человек →
     * матч, если min_persons <= 50.
     */
    SINGLE_LTE,

    /**
     * Вариант хранит диапазон [min; max], клиентское значение должно попасть внутрь.
     * Пример: вместимость зала 50-150, клиент указал 100 → матч.
     */
    RANGE_CONTAINS,

    /**
     * Точное совпадение булевых значений.
     * Пример: клиент хочет наличие сцены → матч, если has_stage = true.
     */
    BOOLEAN_MATCH,

    /**
     * Массив варианта содержит клиентское значение.
     * Пример: массив languages варианта содержит "KK" → матч.
     */
    ARRAY_CONTAINS,

    /**
     * Массив варианта пересекается с клиентским массивом.
     * Пример: вариант говорит ["KK","RU"], клиент хочет ["RU","EN"] → матч (пересечение RU).
     */
    ARRAY_INTERSECTS
}